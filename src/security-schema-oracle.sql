CREATE TABLE ZZ_USERS(
      USER_NAME VARCHAR2(50) NOT NULL PRIMARY KEY,
      USER_PW VARCHAR2(50) NOT NULL,
      USE_FLAG CHAR(1) NOT NULL
      );
      
CREATE TABLE ZZ_GROUPS (
  	GROUP_ID NUMBER PRIMARY KEY, 
  	GROUP_NAME VARCHAR2(1000) NOT NULL
  	);      
    
CREATE TABLE ZZ_GROUP_MEMBERS (
  	GROUP_MEMBERS_ID NUMBER PRIMARY KEY, 
  	USER_NAME VARCHAR2(50) NOT NULL, 
  	GROUP_ID NUMBER NOT NULL, 
  	CONSTRAINT FK_GROUP_MEMBERS_GROUP 
  		FOREIGN KEY(GROUP_ID) REFERENCES ZZ_GROUPS(GROUP_ID)
  	);    
    
CREATE TABLE ZZ_GROUP_AUTHORITIES (
  	GROUP_ID NUMBER NOT NULL, 
  	AUTHORITY VARCHAR2(50) NOT NULL, 
  	CONSTRAINT FK_GROUP_AUTHORITIES_GROUP 
  			FOREIGN KEY(GROUP_ID) REFERENCES ZZ_GROUPS(GROUP_ID)
  	);    